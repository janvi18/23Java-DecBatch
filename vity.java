public class vity {

  public static void main(String args[]) {
    String city = "New York";
    switch (city) {
      case "New York":
        System.out.println("New York is a city in the United States");
        break;
      case "London":
        System.out.println("London is a city in the United Kingdom");
        break;
      case "Paris":
        System.out.println("Paris is a city in France");
        break;
      default:
        System.out.println("I don't know where that is");
    }
  }
}
 